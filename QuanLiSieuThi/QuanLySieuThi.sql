create proc HOADONTOHANGHOADELETE @MAHH NVARCHAR(15)
as
DELETE FROM HANGHOA where MAHH = @MAHH
go

create proc HOADONADD @SL int, @MAHH NVARCHAR(15)
as
UPDATE HANGHOA set SOLUONG = SOLUONG - @SL
where MAHH = @MAHH
go

create proc HOADONMINUS @SL int, @MAHH NVARCHAR(15)
as
UPDATE HANGHOA set SOLUONG = SOLUONG + @SL
where MAHH = @MAHH
go

